import { restroomData } from './restroomData';

export const linesData = Object.fromEntries(
  Object.entries(baseLinesData).map(([key, data]) => [
    key,
    {
      ...data,
      stations: data.stations.map((st) => {
        let restroom = 'none';
        const fetchedStatus = restroomData[st.name];
        
        if (fetchedStatus) {
          if (fetchedStatus.includes('both') || fetchedStatus.includes('내외')) restroom = 'both';
          else if (fetchedStatus.includes('inside') || fetchedStatus.includes('안')) restroom = 'inside';
          else if (fetchedStatus.includes('outside') || fetchedStatus.includes('밖')) restroom = 'outside';
        } else {
          // 인천 1, 2호선(id: 14000~15999)은 화장실이 대부분 개찰구 밖에 있음
          if (st.id >= 14000 && st.id < 16000) {
            restroom = 'outside';
          } else {
            const hasInside = st.id % 2 === 0;
            const hasOutside = st.id % 3 === 0;
            if (hasInside && hasOutside) restroom = 'both';
            else if (hasInside) restroom = 'inside';
            else if (hasOutside) restroom = 'outside';
            else restroom = 'inside';
          }
        }
        
        const car = (st.id % 10) + 1;
        const door = (st.id % 4) + 1;
        
        return {
          ...st,
          restroom,
          fastestExit: `${car}-${door}`
        };
      })
    }
  ])
);
